    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        String codigo = jTCodigo.getText();
        String anio = jTAnio.getText();
        

    
        
    if (!codigo.matches("\\d+")) {
        JOptionPane.showMessageDialog(this, "En el campo CODIGO se deben introducir numeros ", "Error de entrada", JOptionPane.ERROR_MESSAGE);
        return; 
    }

   
    if (!anio.matches("[a-zA-Z]+")) {
        JOptionPane.showMessageDialog(this, "En el campo AÃ‘O se deben introducir caracteres ", "Error de entrada", JOptionPane.ERROR_MESSAGE);
        return; 
    }
    
   
    
    
      
    
    }//GEN-LAST:event_jBGuardarActionPerformed

